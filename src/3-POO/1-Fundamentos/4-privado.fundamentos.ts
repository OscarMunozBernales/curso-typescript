/**
 * ¿Qué quiere decir?
 * Podemos proteger nuestras propiedades y métodos para que nos sean accedidos y modificados por fuera de clase.
 * 
 * Private
 * Con esta palabra reservada private estamos restringiendo el acceso de nuestros parámetros y métodos, solo podrán 
 * ser accedidos o modificados dentro de la clase.
 * 
 * Private class fields (#)
 * A desde hace muy poco JavaScript tiene incorporado la capacidad de poder limitar el alcance de los métodos y propiedades 
 * utilizando #. Funciona igual que private, solo que # es nativo de JavaScript y realmente limita el alcance.
 */