/**
 * ¿Qué quiere decir acceso público? 
 * Esto quiere decir que podemos acceder a las propiedades y métodos del objeto desde el exterior.
 * 
 * Public
 * Por defecto, todas las propiedades y métodos son públicos pero si queremos aclarar en nuestro código 
 * el alcance de los mismos podemos utilizar la palabra reservada public.
 * 
 * Desventajas del alcance público
 * La desventaja que tenemos al tener una propiedad o método con alcance público es que estás pueden ser
 * modificables desde fuera de la clase.
 */